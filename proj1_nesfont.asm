# -------------------------------------------------------------------------------------------------

# loads NES font at end of tilemap graphics RAM and hooks it up to the text subsystem
load_nes_font_tm:
	push ra
	la a0, nes_font_xlate
	jal display_set_font_xlate_table
	pop ra

	la a0, nes_font_graphics
	li a1, NES_FONT_TILE_START
	li a2, NUM_NES_FONT_TILES
	j  display_load_tm_gfx

# -------------------------------------------------------------------------------------------------

# loads NES font at end of sprite graphics RAM and hooks it up to the text subsystem
load_nes_font_sprite:
	push ra
	la a0, nes_font_xlate
	jal display_set_font_xlate_table
	pop ra

	la a0, nes_font_graphics
	li a1, NES_FONT_TILE_START
	li a2, NUM_NES_FONT_TILES
	j  display_load_sprite_gfx
